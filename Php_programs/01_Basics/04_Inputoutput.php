<?php

// console only
echo "enter something: ";

/*
    readline() is a function used to read a line of input from the user via the command line interface (CLI). 
    It allows users to enter text interactively and returns the input as a string.
*/
$num = readline();
echo "You entered: $num\n";

/*
    Output:
        enter something: Virat Kholi
        You entered: Virat Kholi
*/
?>
<?php
// If else statement

/*
    If else statement:
        the if-else statement is used to execute one block of code if a specified condition evaluates to true, 
        and another block of code if the condition evaluates to false.
*/

$a = 9;

if($a > 10){
    print("a is greater than 10<br>\n");
}else{
    print("a is less than 10<br>\n");
}

/*
    Output:
        a is less than 10<br>
*/
?>
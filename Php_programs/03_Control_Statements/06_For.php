<?php
// For Loop

/*
    For Loop:
        the for loop is used to execute a block of code a specified number of times. 
        It's particularly useful when you know in advance how many times you want the loop to run. 
*/

for($i = 0; $i < 10; $i++){
    echo "dora $i <br>\n";
}


/* 
    Output:
        dora 0 <br>
        dora 1 <br>
        dora 2 <br>
        dora 3 <br>
        dora 4 <br>
        dora 5 <br>
        dora 6 <br>
        dora 7 <br>
        dora 8 <br>
        dora 9 <br>
*/
?>